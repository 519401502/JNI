package com.example.aml.clearn.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

/**
 * Created by 18624915319 on 2017/9/25.
 */

public class ViewPagerAdapter_ extends FragmentPagerAdapter {

    private List<Fragment> fragmentList;

    public ViewPagerAdapter_(FragmentManager fm, List<Fragment> fragmentList) {
        super(fm);
        this.fragmentList = fragmentList;
    }

    @Override
    public Fragment getItem(int position) {
        return fragmentList.get(position);
    }

    @Override
    public int getCount() {
        return fragmentList.size();
    }
}
